# Импорт базового класса Enum для создания перечислений
# Enum - это специальный тип, который позволяет создавать набор именованных констант
from enum import Enum


# ============================================
# ПЕРЕЧИСЛЕНИЕ ДЛЯ РЕЖИМОВ ЧТЕНИЯ ФАЙЛОВ
# ============================================

class FileReadMode(str, Enum):
    """
    Перечисление для режимов чтения файлов.

    Что это такое?
    - Это способ определить, в каком формате читать файл
    - string - читать как текст (UTF-8)
    - bytes - читать как бинарные данные

    Пример использования:
    - if mode == FileReadMode.string: читаем как текст
    - if mode == FileReadMode.bytes: читаем как байты

    Зачем это нужно?
    - Безопасность типов: нельзя случайно использовать неправильный режим
    - Читаемость кода: понятно, что означают "string" и "bytes"
    """
    # Режим чтения файла как текстовой строки
    # Используется для .txt, .py, .json и других текстовых файлов
    string = ("string",)

    # Режим чтения файла как бинарных данных
    # Используется для изображений, исполняемых файлов, сжатых файлов
    bytes = ("bytes",)


# ============================================
# ПЕРЕЧИСЛЕНИЕ ДЛЯ РЕЖИМОВ ОТОБРАЖЕНИЯ
# ============================================

class FileDisplayMode(str, Enum):
    """
    Перечисление для режимов отображения файлов в команде ls.

    Что это такое?
    - Определяет уровень детализации информации о файлах
    - simple - только имена файлов
    - detailed - полная информация (размер, дата, права доступа)

    Пример использования:
    - ls (без флагов) - используется FileDisplayMode.simple
    - ls --detailed - используется FileDisplayMode.detailed

    Зачем это нужно?
    - Единый способ определения режима отображения
    - Можно легко добавить новые режимы (например, json, xml)
    """
    # Простой режим отображения - только имена файлов
    # Пример вывода:
    # file1.txt
    # file2.py
    # folder1/
    simple = "simple"

    # Подробный режим отображения - с информацией о размере, дате и правах доступа
    # Пример вывода:
    # drwxr-xr-x      4096 2024-01-15 10:30:45 folder1
    # -rw-r--r--       256 2024-01-15 09:20:30 file1.txt
    detailed = "detailed"
